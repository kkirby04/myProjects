if __name__ == '__main__':
    # Read the first integer from STDIN
    a = int(input())
    
    # Read the second integer from STDIN
    b = int(input())
    
    # Perform integer division using //
    integer_division = a // b
    # Print the result of integer division
    print(integer_division)
    
    # Perform float division by converting integers to floats
    float_division = float(a) / float(b)
    # Print the result of float division
    print(float_division)
